package loops;

public class ForLoopExample {

	int noOfChocolates;

	// print the next ten numbers.

	void printConsecutiveTenNumbersAfterFive() {

		// syntax of loop //for(int i=0;i<5;i++)
		// initialize counter - assign value to variable, check condition - boolean,
		// increment counter - perform the increment

		for 
		(int hno = 0; 
				hno < 10; 
				hno++) {
			System.out.println(++noOfChocolates);}
		
	}

}
